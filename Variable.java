public class Variable{
    public static void main(String[] args)
    {
        int age=18;
        System.out.println(age);
        System.out.println("age");
        //Long variable
        //long is used to store large numbers
        //long is 64 bits
        //int is 32 bits
        long population=1000000000L;
        System.out.println(population);

        //Float variable
        //float is used to store decimal numbers
        //float is 32 bits
        float price=19.9f;
        System.out.println(price);

        //Double variable       
        //double is used to store decimal numbers
        //double is 64 bits
        double distance=12345.6789;
        System.out.println(distance);

        //Character variable        
        //char is used to store a single character
        //char is 16 bits
        //it will take  2 bytes of memory
        char grade='A';
        System.out.println(grade);  

        //Boolean variable      
        //boolean is used to store true or false values
        //boolean is 1 bit
        boolean isPassed=true;
        System.out.println(isPassed);

        //Task

        String name="John Doe";
        int a = name.length();
        System.out.println("Length of name: " + a);




    }
    
}